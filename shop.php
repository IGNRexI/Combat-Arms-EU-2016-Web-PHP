            <h3 class="custom">Modifikasyon</h3>
            <img src="img/guide/00G7O-2b80f652-90ff-44f7-8cbb-fa06ed0c67ca.jpg" alt="" class="img4">
            <p class="custom1">
                Mağazadaki Modifikasyon bölümü, bazı mevcut silahların özel özelleştirme bileşenlerini eklemeniz, bazı mevcut silahların istatistiklerini değiştirmenize izin verecektir.<br><br>
                Bir silahı özelleştirmek için:<br><br>
            </p>
            <ul class="custom1">
                <li>
                    - Özelleştirmek istediğiniz silahı ''Silah Seçme'' listesinden seçin.
                </li>
                <li>
                    - Sadece belirli silahlar özelleştirilebilir. Ancak, modifikasyon penceresinde görünen tüm silahlar özelleştirilebilir.
                </li>
                <li>
                    - Uygun silahlarda ''özelleştirme slotları'' bulunur. Bu slotlar bu silaha ne kadar özelleştirme yapılabileceğini gösterir. Uyarı! Özelleştirme başarılı olsun yada olmasın, Modifikasyon Parçaları harcanır.
                </li>
                <li>
                    - Modifikasyon slotundan, işlem yapılacak olan slotu seçiniz.
                </li>
                <li>
                    - Silahın slotunun kaç seviye olacağını belirleyin.
                </li>
                <li>
                    - Özelleştirmenin muhtemel sonuçları seçilen silahın yanında gösterilir.
                </li>
                <li>
                    - Daha yüksek özelleştirme için daha fazla Modifikasyon Parçasına ihtiyaç vardır.
                </li>
                <li>
                    - Eğer isterseniz; Özelleştirmenin başarı şansını arttırmak için GP kullanabilirsiniz.
                </li>
                <li>
                    - İşlemi tamamlamak için '' Özelleştir'' butonuna tıklayınız.
                </li>
                <li>
                    Başarı oranı silahınızın sağ üst tarafında gösterilir. Başarısızlık durumunda Özelleştirme Parçaları kaybolur. Ancak, orjinal silahınızı yada Özelleştirme Slotunu kaybetmezsiniz.
                </li>
            </ul>
